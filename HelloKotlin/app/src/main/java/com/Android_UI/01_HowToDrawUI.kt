package com.Android_UI

// 안드로이드에서 화면을 그리는 방법
// XML -> 안드로이드 UI를 그리기 위해 특화된 언어

// 핸드폰마다 화면 크기가 다 다른데 어떻게 화면을 그려야 할까
// 픽셀, dpi, dp 단위
// 픽셀 -> 핸드폰 화면에서 빛이 나오는 전구 -> 가장 작은 단위

// dpi -> dot per inch -> 1인치에 몇픽셀 들어있는지
// ldpi(120), mdpi(160), hdpi(240), xhdpi(320), xxhdpi(480), xxxhdpi(640)
// 핸드폰마다 크기가 달라짐

// dp -> Density Independent Pixel -> 픽셀 독립적인 단위
// 핸드폰이 달라도 크기가 같음